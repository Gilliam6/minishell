#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	void	*res;
	void	*ch;

	res = s;
	ch = &c;
	while (n-- > 0)
	{
		*((char *)s) = *((char *)ch);
		s++;
	}
	return (res);
}
